// 为特定人格生成精简上下文（用于 API 调用）
export function buildPersonalityContext(
  code: string,
  name: string,
  fourLetter: string | undefined,
  description: string,
  detail: string,
  scores: { E: number; I: number; S: number; N: number; T: number; F: number; J: number; P: number },
  mode: string
): string {
  const nfti = fourLetter
    ? fourLetter.split('').map((l) => {
        const map: Record<string, string> = { E: 'O', I: 'R', S: 'G', N: 'V', T: 'L', F: 'E', J: 'S', P: 'F' }
        return map[l] || l
      }).join('')
    : code

  const dimensionSummary = []
  dimensionSummary.push(`社交电量：${scores.E >= scores.I ? 'O' : 'R'} (${Math.abs(scores.E - scores.I)}分差距)`)
  dimensionSummary.push(`信息偏好：${scores.S >= scores.N ? 'G' : 'V'} (${Math.abs(scores.S - scores.N)}分差距)`)
  if (mode !== 'quick') {
    dimensionSummary.push(`决策风格：${scores.T >= scores.F ? 'L' : 'E'} (${Math.abs(scores.T - scores.F)}分差距)`)
    dimensionSummary.push(`生活节奏：${scores.J >= scores.P ? 'S' : 'F'} (${Math.abs(scores.J - scores.P)}分差距)`)
  }

  return `用户测试结果：${name}（${code} · ${nfti}）
测试模式：${mode === 'quick' ? '快速测试（12题）' : '完整测试（48题）'}
一句话描述：${description}

四维得分：
${dimensionSummary.join('\n')}

人格详情：${detail}`
}
